package org.deegree.desktop.mapmodel;

import java.util.List;

import org.deegree.desktop.config.LayerType;
import org.deegree.desktop.config.LayerType.MetadataURL;
import org.deegree.model.Identifier;

/**
 * An extension of the {@link Layer} for managing layers that has been generated by iGeodesktop containing
 * geo-information that does not have a real-world counterpart; for example: positions of topological errors of loaded
 * geometries.
 * 
 * @author <a href="mailto:name@deegree.org">Andreas Poth</a>
 * @author last edited by: $Author$
 * 
 * @version $Revision$, $Date$
 */
public class SystemLayer extends Layer {

    private boolean visibleInLayerTree = true;

    /**
     * @param layerType
     * @param owner
     * @param datasources
     * @param styles
     */
    public SystemLayer( LayerType layerType, MapModel owner, List<Datasource> datasources, List<NamedStyle> styles ) {
        super( layerType, owner, datasources, styles );
    }

    /**
     * @param owner
     * @param identifier
     * @param title
     * @param abstract1
     * @param datasources
     * @param metadataURLs
     */
    public SystemLayer( MapModel owner, Identifier identifier, String title, String abstract1,
                        List<Datasource> datasources, List<MetadataURL> metadataURLs ) {
        super( owner, identifier, title, abstract1, datasources, metadataURLs );
    }

    /**
     * @param owner
     * @param identifier
     * @param title
     * @param abstract1
     */
    public SystemLayer( MapModel owner, Identifier identifier, String title, String abstract1 ) {
        super( owner, identifier, title, abstract1 );
    }

    public void setVisibleInLayerTree( boolean b ) {
        this.visibleInLayerTree = b;
    }

    public boolean isVisibleInLayerTree() {
        return visibleInLayerTree;
    }

}
